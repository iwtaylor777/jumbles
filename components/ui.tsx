import type { ReactNode, SVGProps } from "react";

export function IconButton({
  label,
  onClick,
  children,
}: {
  label: string;
  onClick?: () => void;
  children: ReactNode;
}) {
  return (
    <button className="iconbtn" aria-label={label} title={label} onClick={onClick}>
      {children}
    </button>
  );
}

